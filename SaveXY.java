package com.java.SnakeEx2;

public class SaveXY {

	int xNumber = 0;
	int yNumber = 0;

	public SaveXY(int xNumber, int yNumber) {

		this.xNumber = xNumber;
		this.yNumber = yNumber;

	}

	public SaveXY() {

	}

	public int getX() {

		return this.xNumber;

	}

	public int getY() {

		return this.yNumber;

	}

	public int setX(int x) {

		return this.xNumber;

	}
	
	public int setY(int y) {

		return this.yNumber;

	}
	
	
}
